// Base property interface without SEO fields
export interface BaseRentalProperty {
  id: number;
  mlsNumber: string;
  address: string;
  subdivision: string;
  sqft: number;
  beds: number;
  baths: number;
  parking: string;
  furnished: boolean | null;
  price: number;
  image: string;
}

// Full property interface with SEO fields
export interface RentalProperty extends BaseRentalProperty {
  slug: string;
  seoTitle: string;
  seoDescription: string;
  keywords: string[];
  imageAlt: string;
  // Structured data for JSON-LD
  structuredData: {
    "@type": "Product";
    name: string;
    description: string;
    image: string;
    offers: {
      "@type": "Offer";
      price: number;
      priceCurrency: string;
      availability: string;
    };
  };
}

// Helper function to generate slug from address
export function generateSlug(address: string): string {
  return address
    .toLowerCase()
    .replace(/[#]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
}

// Helper function to generate SEO title
export function generateSeoTitle(property: BaseRentalProperty): string {
  const furnished = property.furnished ? 'Furnished ' : '';
  return `${furnished}${property.beds} Bed ${property.baths} Bath Home for Rent in ${property.subdivision} | $${property.price.toLocaleString()}/mo`;
}

// Helper function to generate SEO description
export function generateSeoDescription(property: BaseRentalProperty): string {
  const furnished = property.furnished ? 'fully furnished ' : '';
  const parkingInfo = property.parking !== 'None' ? ` with ${property.parking.toLowerCase()} parking` : '';
  return `Beautiful ${furnished}${property.beds} bedroom, ${property.baths} bathroom rental home at ${property.address} in ${property.subdivision}. ${property.sqft.toLocaleString()} sq ft${parkingInfo}. Available for $${property.price.toLocaleString()}/month. MLS# ${property.mlsNumber}. Schedule a viewing today!`;
}

// Helper function to generate keywords
export function generateKeywords(property: BaseRentalProperty): string[] {
  const keywords = [
    'rental property',
    'house for rent',
    `${property.beds} bedroom rental`,
    `${property.baths} bathroom home`,
    property.subdivision.toLowerCase(),
    'Dallas rental',
    'Texas rental property',
    `${property.sqft} sqft home`,
    'Dream Home Investments',
    property.address.split(' ').pop() || '', // Street name
  ];
  
  if (property.furnished) {
    keywords.push('furnished rental', 'furnished home');
  }
  
  if (property.parking !== 'None') {
    keywords.push('garage parking', 'covered parking');
  }
  
  if (property.price >= 5000) {
    keywords.push('luxury rental', 'executive home', 'premium rental');
  }
  
  if (property.sqft >= 3000) {
    keywords.push('large home', 'spacious rental');
  }
  
  return keywords.filter(k => k.length > 0);
}

// Helper function to generate image alt text
export function generateImageAlt(property: BaseRentalProperty): string {
  const furnished = property.furnished ? 'furnished ' : '';
  return `${furnished}${property.beds} bedroom ${property.baths} bathroom rental home at ${property.address}, ${property.subdivision} - ${property.sqft.toLocaleString()} sq ft - $${property.price.toLocaleString()}/month`;
}

// Helper function to generate structured data for JSON-LD
export function generateStructuredData(property: BaseRentalProperty): RentalProperty['structuredData'] {
  return {
    "@type": "Product",
    name: `${property.beds} Bed ${property.baths} Bath Rental at ${property.address}`,
    description: generateSeoDescription(property),
    image: property.image,
    offers: {
      "@type": "Offer",
      price: property.price,
      priceCurrency: "USD",
      availability: "https://schema.org/InStock",
    },
  };
}

// Function to enrich a base property with SEO data
export function enrichPropertyWithSEO(property: BaseRentalProperty): RentalProperty {
  return {
    ...property,
    slug: generateSlug(property.address),
    seoTitle: generateSeoTitle(property),
    seoDescription: generateSeoDescription(property),
    keywords: generateKeywords(property),
    imageAlt: generateImageAlt(property),
    structuredData: generateStructuredData(property),
  };
}

// Get property by slug
export function getPropertyBySlug(slug: string): RentalProperty | undefined {
  return rentalProperties.find(p => p.slug === slug);
}

// Get property by MLS number
export function getPropertyByMLS(mlsNumber: string): RentalProperty | undefined {
  return rentalProperties.find(p => p.mlsNumber === mlsNumber);
}

